//Adição
let a = 10
let b = 5
let c = a + b
console.log(c)

//Subtração
let d = 10
let e = 15
let f = d - e
console.log(f)

//Multiplicação
let g = 10
let h = 5
let i = g * h
console.log(i)

//Divisão
let j = 10
let k = 2
let l = j / k
console.log(l)

//incremento
let m = 10
m++
console.log(m)

//decremento
let n = 10
n--
console.log(n)

//atribuição +
let o = 12
o += 25 //O += significa o = o + 25, ou seja, a váriavel igual a ela mesma mais 25.
console.log(o)

//atribuição -
let p = 12
p -= 25 
console.log(p)